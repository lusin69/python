# Nomes de variáveis
# Uma variável pode ter um nome curto (como x e y) ou um nome mais descritivo (idade, nome do carro, volume_total).
# Regras para variáveis Python:
# 1 - O nome de uma variável deve começar com uma letra ou sublinhado
# 2 - Um nome de variável não pode começar com um numero
# 3 - Um nome de uma variavel só pode conter caracteres alfanumericos e sublinhados (Az,  0-9 e _)
# 4 - Os nomes das variáveis diferenciam maiúsculas de minúsculas (idade, Idade e IDADE são três variáveis diferentes)
# 5 - Um nome de variável não pode ser nenhuma palavra-chave do python

