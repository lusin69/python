# Existem várias técnicas que você pode usar para torna-los mais legíveis:

# Camel case (Cada palavra, exceto a primeira, começa com letra maiúscula:)

myVariableName = 'John'

# Pascal case (Cada palavra começa com uma letra maiúscula):

MyVariableName = 'John'

# snack case (cada palavra é separada por um caractere de sublinhado):

my_variable_name = 'John'
